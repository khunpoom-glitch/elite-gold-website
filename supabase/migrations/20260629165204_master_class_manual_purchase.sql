create table if not exists public.admin_users (
  id uuid primary key default gen_random_uuid(),
  user_id uuid not null unique references auth.users(id) on delete cascade,
  email text not null,
  role text not null default 'admin',
  is_active boolean not null default true,
  created_by uuid references auth.users(id) on delete set null,
  deactivated_by uuid references auth.users(id) on delete set null,
  deactivated_at timestamptz,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint admin_users_role_check
    check (role in ('admin', 'super_admin')),
  constraint admin_users_email_not_blank
    check (length(btrim(email)) > 0)
);

create table if not exists public.course_purchase_requests (
  id uuid primary key default gen_random_uuid(),
  request_number bigint generated by default as identity unique,
  reference_code text generated always as ('EG-MC-' || lpad(request_number::text, 6, '0')) stored unique,
  member_id uuid not null references public.profiles(id) on delete cascade,
  member_email text not null,
  member_name text not null,
  course_slug text not null default 'master-class',
  amount_thb integer not null default 12900,
  status text not null default 'payment_started',
  slip_storage_path text,
  slip_file_name text,
  transfer_note text,
  submitted_at timestamptz,
  reviewed_by uuid references auth.users(id) on delete set null,
  reviewed_at timestamptz,
  review_reason text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint course_purchase_requests_course_check
    check (course_slug = 'master-class'),
  constraint course_purchase_requests_amount_check
    check (amount_thb > 0),
  constraint course_purchase_requests_status_check
    check (status in ('payment_started', 'pending_review', 'approved', 'rejected')),
  constraint course_purchase_requests_member_course_unique
    unique (member_id, course_slug)
);

create table if not exists public.course_entitlements (
  id uuid primary key default gen_random_uuid(),
  member_id uuid not null references public.profiles(id) on delete cascade,
  course_slug text not null default 'master-class',
  purchase_request_id uuid references public.course_purchase_requests(id) on delete set null,
  granted_by uuid references auth.users(id) on delete set null,
  granted_at timestamptz not null default now(),
  created_at timestamptz not null default now(),
  constraint course_entitlements_course_check
    check (course_slug = 'master-class'),
  constraint course_entitlements_member_course_unique
    unique (member_id, course_slug)
);

create table if not exists public.course_purchase_admin_events (
  id uuid primary key default gen_random_uuid(),
  event_type text not null,
  actor_id uuid references auth.users(id) on delete set null,
  target_user_id uuid references auth.users(id) on delete set null,
  purchase_request_id uuid references public.course_purchase_requests(id) on delete cascade,
  metadata jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now(),
  constraint course_purchase_admin_events_type_check
    check (
      event_type in (
        'purchase_started',
        'slip_submitted',
        'purchase_approved',
        'purchase_rejected',
        'admin_added',
        'admin_deactivated'
      )
    )
);

