create or replace function private.is_elite_admin(required_role text default null)
returns boolean
language sql
stable
security definer
set search_path = ''
as $$
  select exists (
    select 1
    from public.admin_users
    where user_id = (select auth.uid())
      and is_active = true
      and (
        required_role is null
        or role = required_role
        or (required_role = 'admin' and role = 'super_admin')
      )
  );
$$;

create or replace function private.current_elite_admin_role()
returns text
language sql
stable
security definer
set search_path = ''
as $$
  select role
  from public.admin_users
  where user_id = (select auth.uid())
    and is_active = true
  order by
    case role
      when 'super_admin' then 0
      else 1
    end
  limit 1;
$$;

